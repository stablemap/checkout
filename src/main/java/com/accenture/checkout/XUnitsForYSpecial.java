package com.accenture.checkout;

public class XUnitsForYSpecial {

  private int count;
  private int price;

  public XUnitsForYSpecial(int count, int price) {
    this.count = count;
    this.price = price;
  }

  public int getCount() {
    return count;
  }

  public int getPrice() {
    return price;
  }
}
